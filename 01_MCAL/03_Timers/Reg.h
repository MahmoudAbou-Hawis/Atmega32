#define		TCNT0			*((uint8_t*)0x52)

#define		OCR0			*((uint8_t*)0x5C)






/************************************************************************/
/*			Timer 1 registers 16-bit                        */
/************************************************************************/

#define		TCCR1A			*((uint8_t*)0x4F)

#define		TCCR1B			*((uint8_t*)0x4E)

#define		TCNT1			*((uint16_t*)0x4C)

#define		OCR1A			*((uint16_t*)0x4A)




/************************************************************************/
/*			Timer 2 registers 8-bit                         */
/************************************************************************/

#define		TCCR2			*((uint8_t*)0x45)

#define		TCNT2			*((uint8_t*)0x44)

#define		OCR2			*((uint8_t*)0x43)

#define		ASSR			*((uint8_t*)0x42)



/************************************************************************/
/*                     Enable interrupt register                        */
/************************************************************************/
#define		TIMSK			*((uint8_t*)0x59)




#define		SREG				*((volatile uint8_t *)0x5F)

